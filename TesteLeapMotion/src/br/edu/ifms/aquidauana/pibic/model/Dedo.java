package br.edu.ifms.aquidauana.pibic.model;

public class Dedo {

	private Float distanciaDistal;

	private Float distanciaMedial;

	private Float distanciaMetacarpal;

	private Float distanciaProximal;

	public Float getDistanciaDistal() {
		return distanciaDistal;
	}

	public void setDistanciaDistal(Float distanciaDistal) {
		this.distanciaDistal = distanciaDistal;
	}

	public Float getDistanciaMedial() {
		return distanciaMedial;
	}

	public void setDistanciaMedial(Float distanciaMedial) {
		this.distanciaMedial = distanciaMedial;
	}

	public Float getDistanciaMetacarpal() {
		return distanciaMetacarpal;
	}

	public void setDistanciaMetacarpal(Float distanciaMetacarpal) {
		this.distanciaMetacarpal = distanciaMetacarpal;
	}

	public Float getDistanciaProximal() {
		return distanciaProximal;
	}

	public void setDistanciaProximal(Float distanciaProximal) {
		this.distanciaProximal = distanciaProximal;
	}

}
